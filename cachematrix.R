## Matrix cache function.
## 1. Create a custom matrix instead of ordinary matrix by makeCacheMatrix.
## 2. Call cacheSolve when you want get inverse of the custom matrix. 
##    If the inverse has already been calculated, cacheSolve returns cached value.

## create custom matrix function
makeCacheMatrix <- function(x = matrix()) {
        inv <- NULL
        set <- function(y) {
                x <<- y
                inv <<- NULL
        }
        get <- function() x
        setinv <- function(inverse) inv <<- inverse
        getinv <- function() inv
        list(set = set, get = get,
             setinv = setinv,
             getinv = getinv)
}


## caluculate inverse matrix using cache
cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        inv <- x$getinv()
        if(!is.null(inv)) {
                message("getting cached data")
                return(inv)
        }
        data <- x$get()
        inv <- solve(data, ...)
        x$setinv(inv)
        inv
}
